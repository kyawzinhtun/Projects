package org.amaris.service.lineManager;

public class Line {
	
	private String cuttingId;
	private String lineName;
	private String id;
	private String styleId;
	private String startDate;
	private String endDate;
	private String totalUnit;
	private String receiveUnit;
	private String status;
	
	public Line() {
		
	}

	public Line(String cuttingId, String lineName, String id,
			String styleId, String startDate, String endDate, String totalUnit,
			String receiveUnit, String status) {
		this.cuttingId = cuttingId;
		this.lineName = lineName;
		this.id = id;
		this.styleId = styleId;
		this.startDate = startDate;
		this.endDate = endDate;
		this.totalUnit = totalUnit;
		this.receiveUnit = receiveUnit;
		this.status = status;
	}

	public String getCuttingId() {
		return cuttingId;
	}

	public void setCuttingId(String cuttingId) {
		this.cuttingId = cuttingId;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStyleId() {
		return styleId;
	}

	public void setStyleId(String styleId) {
		this.styleId = styleId;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getTotalUnit() {
		return totalUnit;
	}

	public void setTotalUnit(String totalUnit) {
		this.totalUnit = totalUnit;
	}

	public String getReceiveUnit() {
		return receiveUnit;
	}

	public void setReceiveUnit(String receiveUnit) {
		this.receiveUnit = receiveUnit;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
