package org.amaris.service.balanceManage;

import java.util.List;

public class CuttingDetail {
	
	private String cuttingId;
	private List<StockBalance> stockList;
	
	public CuttingDetail() {
		
	}

	public String getCuttingId() {
		return cuttingId;
	}

	public void setCuttingId(String cuttingId) {
		this.cuttingId = cuttingId;
	}

	public List<StockBalance> getStockList() {
		return stockList;
	}

	public void setStockList(List<StockBalance> stockList) {
		this.stockList = stockList;
	}
	
	

}
