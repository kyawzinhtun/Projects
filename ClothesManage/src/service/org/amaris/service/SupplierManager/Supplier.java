package org.amaris.service.SupplierManager;

public class Supplier {
	private String id;
	private String supplierName;
	private String supplierAddress;
	private String supplierPh;
	
	public Supplier() {
		
	}

	public Supplier(String id, String supplierName, String supplierAddress,
			String supplierPh) {
		this.id = id;
		this.supplierName = supplierName;
		this.supplierAddress = supplierAddress;
		this.supplierPh = supplierPh;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSupplierName() {
		return supplierName;
	}

	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}

	public String getSupplierAddress() {
		return supplierAddress;
	}

	public void setSupplierAddress(String supplierAddress) {
		this.supplierAddress = supplierAddress;
	}

	public String getSupplierPh() {
		return supplierPh;
	}

	public void setSupplierPh(String supplierPh) {
		this.supplierPh = supplierPh;
	}
	
	
	
}
