package org.amaris.service.cuttingManage;

public class DraffCut {
	
	public String cuttingId;
	public String draffId;
	public String stockId;
	public String perUnitLength;
	public String totalUnit;
	public String totalUnitLength;
	public String realWithDraw;
	public String size;
	public String type;
	
	public DraffCut() {
		
	}

	public String getCuttingId() {
		return cuttingId;
	}

	public void setCuttingId(String cuttingId) {
		this.cuttingId = cuttingId;
	}

	public String getDraffId() {
		return draffId;
	}

	public void setDraffId(String draffId) {
		this.draffId = draffId;
	}

	public String getStockId() {
		return stockId;
	}

	public void setStockId(String stockId) {
		this.stockId = stockId;
	}

	public String getPerUnitLength() {
		return perUnitLength;
	}

	public void setPerUnitLength(String perUnitLength) {
		this.perUnitLength = perUnitLength;
	}

	public String getTotalUnit() {
		return totalUnit;
	}

	public void setTotalUnit(String totalUnit) {
		this.totalUnit = totalUnit;
	}

	public String getTotalUnitLength() {
		return totalUnitLength;
	}

	public void setTotalUnitLength(String totalUnitLength) {
		this.totalUnitLength = totalUnitLength;
	}

	public String getRealWithDraw() {
		return realWithDraw;
	}

	public void setRealWithDraw(String realWithDraw) {
		this.realWithDraw = realWithDraw;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	

}
